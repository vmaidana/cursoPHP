<?php 

    $a = 10;

    echo $a++; // imprime valor de a e soma 1 depois

    echo "<br>";

    echo $a; // valor de a mais 1

    echo "<br>";

    echo ++$a; //soma mais 1 antes e imprime valor de a

    echo "<br>";

    echo $a--; // imprime valor de a e subtrai 1 depois

    echo "<br>";

    echo $a; // valor de a menos 1

    echo "<br>";

    echo --$a; // subtrai 1 antes e imprime valor de a

?>